let fahrenhit = 50;

// calculate celsius and store in celsius variable
// print that value
let celsius = (fahrenhit - 32) * (5 / 9);
console.log(celsius + 'c');

// calculate kelvin value and store in variable
// print that value
let kelvin = (fahrenhit - 32) * (5 / 9) + 273.15
console.log(kelvin + 'k');